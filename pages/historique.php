                Durant cette période, le Club a pris conscience de l'évolution du rapport adhérent/Association. Les adhérents sont devenus des consommateurs qui réclament un produit
                de qualité à un prix acceptable.<br>
                Le Club a pratiqué une politique visant à la qualité du service rendu : 
                <ul>
                    <li>En améliorant les conditions  d'accueil des gymnastes</li>
                    <li>En investissant dans un matériel sécurisé et adapté à la pratique de la Gymnastique</li>
                    <li>En formant ses cadres salariés et bénévoles</li>
                    <li>En créant des sections adaptées à la demande</li>
                    <li>En garantissant une cotisation tout compris</li>
                </ul>
