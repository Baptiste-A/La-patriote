            <h1>Un peu d'histoire</h1>

            La Patriote Limousine et Jeunesses Coopératives (PLJC Limoges) réunies est née de la fusion de 2 Clubs en 1962 : La Patriote Limousine et Les Jeunesses Coopératives.<br>
            Le Club des Jeunesses coopératives fut créé en 1920 par les responsables de l'Union de Limoges (un des ancêtres de COOP Atlantique créé en 1881) qui décidèrent d'apporter
            une attention particulière à la jeunesse en développant des activités de loisirs et de détentes pour leur permettre "d'échapper à la rue"... <br>
            Ainsi naquit le cercle des Jeunesses Coopératives qu proposait des activités de gymnastique, de photographie et de chants féminins. Seule la section Gym a subsisté, rejointe
            ensuite par une section Basket. Toutes les 2 sont gracieusement hébergées dans des locaux entretenus et appartenant à Coop Atlantique (Gymnase Avenue Montjovis et salle de
            Basket en Bords de Vienne). <br>
            Le Club de La Patriote Limousine (il faudrait plutôt dire la Société Sportive) a été créé en 1886 par Louis MOREL. La Patriote Limousine est aujourd'hui la plus ancienne
            société sportive de Limoges. <br>
            En 1905, un des ses membres, Marcel LALU (1882-1951), devient champion du Monde de Gymnastique lors des Championnats du Monde organisés à Bordeaux. Il fut également 3 fois
            Champion de France de Gymnastique (1901-1905-1910). <br>
            Il sera le 1er Limougeaud à participer aux Jeux Olympiques à Londres en 1908 ( il terminera 7ème du Concours Général) puis à Stockolm en 1912 ( il terminera 8ème du Concours
            général) et deviendra jusqu'en 1928 le Capitaine entraîneur de l'Equipe de France de Gymnastique.
            
