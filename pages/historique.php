                    <li>2000 : Participation systématique aux opérations de "fêtes des sports"</li>
                    <li>2001 : Obtention du Label Petite Enfance</li>
                    <li>2002 : Création de la fête de Noël</li>
                    <li>2002 : Création du 1er site Internet de La Patriote</li>
                    <li>2004 : Création d'une signalétique Club</li>
                    <li>2006 : Renouvellement du Label Petite Enfance</li>
                    <li>2009 : Organisation de la 1/2 finale des Championnats de France en partenariat avec le Club d'Aixe sur Vienne</li>
                    <li>2010 : Renouvellemen du Label Petite Enfance</li>
                    <li>2012 : Le Club atteint plus de 300 licenciés</li>
                    <li>2012 : Création du Site Internet Actuel </li>
                    <li>2012 : Création du parcours de Noël nouvelle génération "adultes-enfants"</li>
                    <li>2013 : 345 Licenciés</li>
                    <li>2013 : Kévin Le Foll devient Champion de France au Saut, Catégorie optionnel B</li>
                    <li>2014 : Renouvellement du Label Petite Enfance</li>
                    <li>2016 : Le 25 juin,  la Patriote Limousine fête ses 130 ans à Beaublanc </li>
                    <li>2017 : Les 13 et 14 Mai, le Club Organise pour la 1ère fois une Finale de Zone individuelle regroupant environ 800 gymnastes sur 2 jours à la salle
                            HENRI NORMAND</li>
