            <h1>L'histoire contemporaine de 2000 à 2017</h1>

                La patriote Limousine et jeunesses Coopératives réunies, est aujourd'hui le plus ancien Club  Sportif de limoges. <br>
                Fondé en 1886, ce Club aurait pu continuer à vivre doucement de sa notoriété, au rythme des saisons qui s'écoulent. <br>
                En 1999, le nombre des licenciés n'était plus que de 104 et le nombre de bénévoles ne cessait de diminuer. <br>
                2 décisions capitales ont alors été prises : 
                
                <ol>
                    <li>Employer un salarié capable d'assurer l'encadrement permanent des séances, mais aussi de concevoir et déployer des projets de développement.</li>
                    <li>Participer aux formations de dirigeants sur les thèmes du développement associatif et définir une politique de développement.</li>
                </ol>
                Le Club de La Patriote Limousine enregistre fin 2005, 200 licenciés puis atteint un record historique de plus de 300 licenciés à partir de la saison 2012-2013,
                et 345 en  2013-2014. <br>
                La patriote Limousine est aujourd'hui le 3ème Club de Gymnastique de la Région Limousin en nombre de licenciés. <br>
                Le nombre de bénévoles intervenants pour l'encadrement des séances ou pour l'organisation des manifestations est en constante augmentation :
                les bénévoles trouvent aujourd'hui leur compte dans une structure dynamique et motivante. <br>
                Ces chiffres reflètent toute une politique de déceloppement qui se traduit par des actions majeures telles <br>
                <ul>
                    <li>l'Opération Petite Enfance qui a amené le Club à obtenir le Label "Petite Enfance" décerné par la FFGym </li>
                    <li>les partenariats avec les collectivités locales (mercredis sportifs, sportez-vous bien, , interventiosn dans les écoles)</li>
                    <li>les animations de rentrée scolaire (Coeur de Limoges-Place de la Motte et Vitalsports - Décathlon Zone Nord)</li>
                </ul>
                Toute cette politique a pour but d'ouvrir le Club, d'élargir sa base, pour pouvoir mieux revenir sur ses activités traditionnelles : La Gymnastique de Compétition.<br>
                Dans cette optique, de très nombreuses nouvelles sections ont été ouvertes, permettant d'accueillir tous les mebres d'une famille de tout âge et de tout niveau de pratique. 

                <br><br><br><br><br><br><br>

